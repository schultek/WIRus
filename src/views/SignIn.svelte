<script>
  import firebase from "../lib/firebase";
  import { testEmail } from "../lib/utils";

  let clientPromise = fetch("/api/auth/info" + window.location.search)
    .then(res => {
      if (res.status == 200) {
        return res.json();
      } else {
        return res.text().then(e => {
          throw new Error(e);
        });
      }
    })
    .then(client => {
      console.log(client);
      return client;
    });

  $: email = "";
  $: emailValid = true;
  $: password = "";
  $: credValid = true;

  $: loggingin = false;

  const signin = client => {
    if (loggingin) return;

    if (!testEmail(email)) {
      emailValid = false;
    } else {
      console.log("LOGIN");
      loggingin = true;
      firebase
        .auth()
        .signInWithEmailAndPassword(email, password)
        .then(credential => {
          return credential.user.getIdToken().then(token => {
            window.location = `/api/auth/goto?authorization=${token}&${window.location.search.replace(
              "?",
              ""
            )}`;
          });
        })
        .catch(err => {
          console.warn(err);
          credValid = false;
        });
    }
  };
</script>

